export const TIME_SLOTS = (() => {
  const out = []
  for (let h = 0; h < 24; h++) {
    for (let m = 0; m < 60; m += 15) {
      const period = h < 12 ? 'AM' : 'PM'
      const hour12 = h % 12 === 0 ? 12 : h % 12
      out.push(`${hour12}:${m.toString().padStart(2, '0')} ${period}`)
    }
  }
  return out
})()

